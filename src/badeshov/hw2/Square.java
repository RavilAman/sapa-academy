package badeshov.hw2;

public class Square {

    public void perimeterOfSquare() {

        double a = 7;
        double perimeter = a * 4;
        System.out.println("Perimeter of Square = " + perimeter);
    }

    public void areaOfSquare() {

        double a = 7;
        double area = a * a;
        System.out.println("Area of Square = " + area);
    }
}
